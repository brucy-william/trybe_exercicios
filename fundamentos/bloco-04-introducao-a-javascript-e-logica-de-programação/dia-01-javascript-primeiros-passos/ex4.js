//Faça um programa que, dado um valor recebido como parâmetro, retorne 
//"positive" se esse valor for positivo, "negative" se for negativo, e 
//caso não seja nem positivo e nem negativo retorne "zero".

const parameter = 0;

if (parameter > 0){
    console.log("Esse numero é Positivo");
} else if (parameter < 0) {
    console.log("Esse numero é Negativo");
} else {
    console.log("Esse numero é zero");
}
